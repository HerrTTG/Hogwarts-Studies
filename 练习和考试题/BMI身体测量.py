# 同时接受两个输入
# 同时判断两个条件
# 同时输出两个

身高, 体重 = eval(input())

bmi = 体重 / pow(身高, 2)
print("BMI数值为:{:.2f}".format(bmi))
国内, 国际 = "", ""
if bmi < 18.5:
    国内, 国际 = "偏瘦", "偏瘦"
elif 18.5 <= bmi < 24:
    国内, 国际 = "正常", "正常"
elif 24 <= bmi < 25:
    国内, 国际 = "偏胖", "正常"
elif 25 <= bmi < 28:
    国内, 国际 = "偏胖", "偏胖"
elif 28 <= bmi < 30:
    国内, 国际 = "肥胖", "偏胖"
else:
    国内, 国际 = "肥胖", "肥胖"
print("BMI指标为:国际'{0}',国内'{1}'".format(国际, 国内))
